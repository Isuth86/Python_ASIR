# Ejercicio 2: Programa que muestre los números pares comprendidos entre el 1 y el 200. Para ello utiliza
# un contador y suma de 2 en 2.

# Mostrar los números pares entre 1 y 200 usando contador +2

contador = 2   # empezamos en el primer número par

while contador <= 200:
    print(contador)
    contador += 2
