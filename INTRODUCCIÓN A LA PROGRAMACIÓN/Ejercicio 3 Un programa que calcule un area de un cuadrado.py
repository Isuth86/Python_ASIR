# Ejercicio 3 Un programa que calcule el area de un cuadrado cuyo lado se introduce
# por teclado (El área del cuadrado es igual a lado por lado)

lado = float(input("Introduce el valor del lado del cuadrado: "))

# Calculamos el área (lado * lado)
area = lado * lado

# Mostramos el resultado
print("El área del cuadrado es:", area)
