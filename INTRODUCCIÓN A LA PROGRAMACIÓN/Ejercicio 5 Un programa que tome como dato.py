# Ejercicio 5 Un programa que toma como dato de entrada un número que corresponde a la
# longitud de un radio.
radio = float(input("Ingrese la longitud del radio: "))

pi = 3.1416

area = pi * radio * radio
circunferencia = 2 * pi * radio

print("Area del círculo:", area)
print("Longitud de la circunferencia:", circunferencia)
