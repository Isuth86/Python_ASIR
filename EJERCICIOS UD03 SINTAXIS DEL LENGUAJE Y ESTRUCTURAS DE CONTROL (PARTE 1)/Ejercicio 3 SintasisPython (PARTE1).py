# 3. Ejercicio - Escriba un programa que calcule el 치rea de un cuadrado cuyo lado se introduce por teclado, (El 치rea del cuadrado es igual a lado por lado).

# * Solicita al usuario que introduzca el valor del lado
lado = float(input("Introduce el valor del lado del cuadrado: "))

# * Calcula el 치rea (lado por lado)
area = lado * lado

# * Muestra el resultado
print("El 치rea del cuadrado es:", area)
